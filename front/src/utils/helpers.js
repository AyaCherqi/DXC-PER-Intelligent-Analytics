// Simple pub/sub event bus
class EventBus {
    constructor() { this.events = {}; }
    on(event, fn) {
        if (!this.events[event]) this.events[event] = [];
        this.events[event].push(fn);
        return () => this.off(event, fn);
    }
    off(event, fn) {
        if (!this.events[event]) return;
        this.events[event] = this.events[event].filter(f => f !== fn);
    }
    emit(event, data) {
        if (!this.events[event]) return;
        this.events[event].forEach(fn => fn(data));
    }
}

export const bus = new EventBus();

// DOM helpers
export function el(tag, attrs = {}, children = []) {
    const element = document.createElement(tag);
    Object.entries(attrs).forEach(([key, value]) => {
        if (key === 'className') element.className = value;
        else if (key === 'innerHTML') element.innerHTML = value;
        else if (key === 'textContent') element.textContent = value;
        else if (key.startsWith('on')) element.addEventListener(key.slice(2).toLowerCase(), value);
        else if (key === 'style' && typeof value === 'object') Object.assign(element.style, value);
        else if (key === 'dataset') Object.entries(value).forEach(([k, v]) => element.dataset[k] = v);
        else element.setAttribute(key, value);
    });
    children.forEach(child => {
        if (typeof child === 'string') element.appendChild(document.createTextNode(child));
        else if (child instanceof Node) element.appendChild(child);
    });
    return element;
}

export function $(selector) { return document.querySelector(selector); }
export function $$(selector) { return document.querySelectorAll(selector); }

// Formatting
export function formatNumber(n, decimals = 0) {
    return new Intl.NumberFormat('en-US', { minimumFractionDigits: decimals, maximumFractionDigits: decimals }).format(n);
}

export function formatPercentage(n) { return `${n}%`; }

export function formatTime(iso) {
    return new Date(iso).toLocaleTimeString('en-US', { hour: '2-digit', minute: '2-digit', hour12: false });
}

export function formatTimeAgo(iso) {
    const diff = Math.floor((Date.now() - new Date(iso).getTime()) / 60000);
    if (diff < 1) return 'Just now';
    if (diff < 60) return `${diff}m ago`;
    if (diff < 1440) return `${Math.floor(diff / 60)}h ago`;
    return `${Math.floor(diff / 1440)}d ago`;
}

export function getStatusColor(status) {
    const colors = { operational: 'var(--accent-green-bright)', warning: 'var(--accent-amber)', critical: 'var(--accent-red)', offline: 'var(--text-muted)' };
    return colors[status] || colors.operational;
}

export function getSeverityClass(severity) {
    return `severity-${severity}`;
}

export function getSeverityBadgeClass(severity) {
    const map = { critical: 'badge-critical', high: 'badge-high', medium: 'badge-medium', low: 'badge-low' };
    return map[severity] || 'badge-medium';
}

export function getAlertStatusLabel(status) {
    const map = { new: 'New', acknowledged: 'Acknowledged', in_progress: 'In Progress', resolved: 'Resolved', escalated: 'Escalated' };
    return map[status] || status;
}

